package level3.ex1.model;

public class Calculator {

    public int suma(int i, int i1) {
        int result = i + i1;
        return result;
    }

    public int substraccion(int i, int i1) {
        int result = i - i1;
        return result;
    }

    public int multiplication(int i, int i1) {
        int result = i * i1;
        return result;
    }

    public int division(int i, int i1) {
        int result = i / i1;
        return result;
    }
}
